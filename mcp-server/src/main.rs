fn sdd_next_step_response(input: &SddNextStepInput) -> String {
    let files = input.existing_files.to_lowercase();

    let has_constitution = files.contains("constitution.md") || files.contains(".specify");
    let has_spec = files.contains("spec.md");
    let has_needs_clar = files.contains("[needs clarification");
    let has_plan = files.contains("plan.md");
    let has_tasks = files.contains("tasks.md");
    let has_src = files.contains("lib/")
        || files.contains("src/")
        || files.contains("lib\\")
        || files.contains("src\\");
    let has_findings = files.contains("findings.md") || files.contains("finding");

    if !has_constitution {
        return "▶ NEXT STEP: spec_constitution\n\
No .specify/constitution.md found. \
This is a new project — call spec_constitution to establish the mobile architecture principles, \
26 patterns, quality gates, and security non-negotiables before writing any spec."
            .to_string();
    }

    if !has_spec {
        return "▶ NEXT STEP: spec_feature\n\
Constitution exists but no spec.md found. \
Call spec_feature with a description of the feature you want to build."
            .to_string();
    }

    if has_needs_clar {
        return "▶ NEXT STEP: spec_clarify\n\
spec.md contains [NEEDS CLARIFICATION] markers. \
Call spec_clarify with the full spec.md content to extract and resolve every ambiguity \
before proceeding to planning."
            .to_string();
    }

    if !has_plan {
        return "▶ NEXT STEP: spec_plan\n\
spec.md is clarified but no plan.md found. \
Call spec_plan to select mobile patterns, generate the implementation plan, data model, \
navigation contracts, and research artifacts."
            .to_string();
    }

    if has_plan && !has_tasks {
        return "▶ NEXT STEP: spec_analyze → then spec_tasks\n\
plan.md exists but no tasks.md. \
First call spec_analyze to verify cross-artifact consistency (spec ↔ plan). \
Once analysis passes, call spec_tasks to generate the ordered task list."
            .to_string();
    }

    if has_tasks && !has_src {
        return "▶ NEXT STEP: scaffold_phase with phase=1\n\
tasks.md is ready and source directory (lib/ or src/) not detected. \
Call scaffold_phase with phase=1 to begin implementing the Domain layer."
            .to_string();
    }

    if has_findings {
        return "▶ NEXT STEP: adjust_finding\n\
findings.md (or equivalent) detected. \
Review each ❌ Critical finding and call adjust_finding for each one, \
then re-run verify_project to confirm resolution."
            .to_string();
    }

    if has_src {
        return "▶ NEXT STEP: verify_project\n\
Source code detected and no open findings file found. \
Call verify_project with plan.md content and a project description to run \
the full compliance checklist."
            .to_string();
    }

    "▶ UNABLE TO DETERMINE NEXT STEP\n\
Please provide a more detailed file listing. Include paths like:\n\
  .specify/constitution.md, specs/NNN-feature/spec.md, specs/NNN-feature/plan.md,\n\
  specs/NNN-feature/tasks.md, lib/, findings.md"
        .to_string()
}

