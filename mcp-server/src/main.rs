#[derive(Clone)]
struct MobilePlaybookServer {
    tool_router: ToolRouter<Self>,
}

impl MobilePlaybookServer {
    fn new() -> Self {
        Self {
            tool_router: Self::tool_router(),
        }
    }
}

#[tool_router(router = tool_router)]
impl MobilePlaybookServer {
    #[tool(description = "
SDD NAVIGATOR — Call any time to find out which SDD step to run next.
Inspects the list of existing project files and determines the current SDD state,
then returns the exact tool to call with the rationale.

Call this tool when:
  - starting a conversation and not sure where in the cycle you are
  - the user asks 'where are we?' or 'what should we do next?'
  - after a context reset and you need to re-orient
  - any time you are unsure which SDD step to run
")]
    fn sdd_next_step(&self, Parameters(input): Parameters<SddNextStepInput>) -> String {
        sdd_next_step_response(&input)
    }

    #[tool(description = "
SDD STEP 0 — CONSTITUTION (run once per project).
Generates the .specify/constitution.md file that governs all subsequent SDD phases.
Embeds the 26 Mobile Architecture patterns, layer rules, quality gates, and security
non-negotiables as the project's immutable principles.

Call this tool when the user asks to:
  - start a new mobile app / feature
  - bootstrap a new Flutter/SwiftUI/Compose/React Native project
  - set up architecture principles for an existing mobile codebase
DO NOT skip this step — constitution is required before spec_feature.
")]
    fn spec_constitution(&self, Parameters(input): Parameters<SpecConstitutionInput>) -> String {
        spec_constitution_response(&input)
    }

    #[tool(description = "
SDD STEP 1 — SPECIFY (define what to build).
Generates specs/NNN-<feature>/spec.md with user stories, acceptance criteria, and
[NEEDS CLARIFICATION] markers for every ambiguity.
Focus strictly on WHAT and WHY — never HOW, never technology, never UI implementation detail.

Call this tool when the user:
  - describes a feature to build
  - says 'implement X' or 'I need Y capability'
  - provides a requirement or user story
After this tool: check for [NEEDS CLARIFICATION] markers → call spec_clarify if any exist.
")]
    fn spec_feature(&self, Parameters(input): Parameters<SpecFeatureInput>) -> String {
        spec_feature_response(&input)
    }

    #[tool(description = "
SDD STEP 2 — CLARIFY ambiguities before planning.
Extracts every [NEEDS CLARIFICATION] marker from spec.md and presents them as explicit
questions for the user to answer. Updates spec.md once all answers are provided.
GATE: Do NOT proceed to spec_plan until this tool reports zero open markers.

Call this tool when:
  - spec.md contains [NEEDS CLARIFICATION] markers
  - the user says 'clarify' or 'what do you need to know'
  - you need to resolve ambiguities before planning
")]
    fn spec_clarify(&self, Parameters(input): Parameters<SpecClarifyInput>) -> String {
        spec_clarify_response(&input)
    }

    #[tool(description = "
SDD STEP 3 — PLAN (define how to build it).
Generates: plan.md (which mobile patterns apply + rationale), data-model.md,
contracts/navigation.md (screen routes + deep links), contracts/api.md (backend API consumed),
and research.md (package/library decisions).
Maps every user story to Mobile Architecture patterns and layers.
GATE: spec.md must have zero [NEEDS CLARIFICATION] markers.

Call this tool when:
  - spec is clarified and user is ready to plan
  - 'create the implementation plan' or 'plan this feature'
  - 'which patterns should I use for this'
")]
    fn spec_plan(&self, Parameters(input): Parameters<SpecPlanInput>) -> String {
        spec_plan_response(&input)
    }

    #[tool(description = "
SDD STEP 4 — ANALYZE cross-artifact consistency.
Verifies spec ↔ plan alignment: every user story covered by a plan section,
every acceptance criteria addressed by a pattern, navigation contracts complete.
GATE: Must report zero critical inconsistencies before calling spec_tasks.

Call this tool when:
  - plan.md has been created and needs validation
  - 'analyze the spec and plan' or 'check consistency' or 'validate the plan'
  - before generating tasks
")]
    fn spec_analyze(&self, Parameters(input): Parameters<SpecAnalyzeInput>) -> String {
        spec_analyze_response(&input)
    }

    #[tool(description = "
SDD STEP 5 — TASKS (generate executable task list).
Generates tasks.md: tasks grouped by layer (Domain → Data → Infrastructure →
Presentation → Tests+CI), with [P] markers for parallel tasks and pattern references.
GATE: spec_analyze must have passed with zero critical inconsistencies.

Call this tool when:
  - analysis passed and user is ready to implement
  - 'generate tasks' or 'create the task list' or 'break this down into steps'
")]
    fn spec_tasks(&self, Parameters(input): Parameters<SpecTasksInput>) -> String {
        spec_tasks_response(&input)
    }

    #[tool(description = "
SDD STEP 6 — IMPLEMENT one phase at a time.
Generates exact implementation instructions for one phase based on tasks.md and plan.md.
Phases: 1=Domain | 2=Data | 3=Infrastructure | 4=Presentation | 5=Tests+CI+Distribution
RULE: Complete each phase and run tests before calling this for the next phase.

Call this tool when:
  - the user is ready to implement a specific phase
  - 'implement phase 1' / 'build the domain layer' / 'scaffold data layer'
  - 'continue to phase N'
")]
    fn scaffold_phase(&self, Parameters(input): Parameters<ScaffoldPhaseInput>) -> String {
        scaffold_phase_response(&input)
    }

    #[tool(description = "
SDD STEP 7 — VERIFY compliance against the Mobile Architecture Playbook.
Runs the compliance checklist filtered to the patterns and features in plan.md.
Returns findings grouped by severity: ❌ Critical | ⚠️ Warning.
RULE: Call this after ALL scaffold_phase steps are complete.

Call this tool when:
  - all phases are implemented
  - 'verify the project' / 'check compliance' / 'audit this app' / 'is this production-ready'
  - 'run the checklist'
")]
    fn verify_project(&self, Parameters(input): Parameters<VerifyProjectInput>) -> String {
        verify_project_response(&input)
    }

    #[tool(description = "
SDD STEP 8 — ADJUST a specific finding from verify_project.
Returns targeted fix instructions for one ❌ Critical or ⚠️ Warning finding.
After applying fixes, call verify_project again on affected sections to re-verify.
Repeat until all critical findings are resolved.

Call this tool when:
  - verify_project returned findings that need to be fixed
  - 'fix finding F-01' / 'how do I resolve this issue' / 'adjust this violation'
  - working through the findings list one by one
")]
    fn adjust_finding(&self, Parameters(input): Parameters<AdjustFindingInput>) -> String {
        adjust_finding_response(&input)
    }
}

