#[derive(Debug, Deserialize, schemars::JsonSchema)]
struct SpecConstitutionInput {
    /// Project / app name in kebab-case (e.g. "my-shopping-app")
    project_name: String,
    /// Programming language (e.g. "Dart", "Swift", "Kotlin", "TypeScript")
    language: String,
    /// UI framework (e.g. "Flutter", "SwiftUI", "Jetpack Compose", "React Native")
    framework: String,
    /// Target platform (e.g. "iOS", "Android", "Cross-platform")
    platform: String,
    /// Primary domain entity in singular form (e.g. "Order", "Product")
    domain_entity: String,
    /// Primary domain entity in plural form (e.g. "Orders", "Products")
    domain_entity_plural: String,
    /// For existing apps only: describe current codebase structure, folder layout,
    /// architectural pattern, and notable conventions.
    /// Leave empty ("") for greenfield — an empty value activates greenfield mode.
    existing_architecture: String,
}

